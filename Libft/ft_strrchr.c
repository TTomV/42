#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	while (i > 0 || c == *s)
	{
		if (s[i] == c)
			return ((char *)s + i);
		i--;
	}
	return (0);
}
